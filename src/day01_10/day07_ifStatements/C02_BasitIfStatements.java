package day01_10.day07_ifStatements;

public class C02_BasitIfStatements {

    public static void main(String[] args) {

        int sayi = 10;

        if (sayi>0){
            System.out.println("sayi poitif");
        }

        if (sayi%2==0){
            System.out.println("sayi cift");
        }

        if (sayi%5==0){
            System.out.println("sayi 5in tam kati");
        }
    }


}
